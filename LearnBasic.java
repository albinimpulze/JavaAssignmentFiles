package LearnLoops;

public class LearnBasic
{
//        Loops :
//        Infinite
//        do while
//        while
//        for
    public static void main(String[] args)
    {
        int x = 1; //11
        do
        {
            System.out.println(x);
            x++;
        }
        while(x<11);

        int y = 1;
        while(y<11)
        {
            System.out.println(y);
            y++;
        }

        for(int i=1;i<11;i++)
        {
            System.out.println(i);

        }






    }

}

